void SlotsQProcess::error ( QProcess::ProcessError error )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "error(QProcess::ProcessError)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM perror = hb_itemPutNI( NULL, (int) error );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, perror );
        hb_itemRelease( psender );
        hb_itemRelease( perror );
      }
    }
  }
}
