#include "QDragSlots.h"

static SlotsQDrag * s = NULL;

SlotsQDrag::SlotsQDrag(QObject *parent) : QObject(parent)
{
}
SlotsQDrag::~SlotsQDrag()
{
  QDrag_release_codeblocks();
}
void SlotsQDrag::actionChanged ( Qt::DropAction action )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "actionChanged(Qt::DropAction)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM paction = hb_itemPutNI( NULL, (int) action );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, paction );
        hb_itemRelease( psender );
        hb_itemRelease( paction );
      }
    }
  }
}
void SlotsQDrag::targetChanged ( QWidget * newTarget )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "targetChanged(QWidget*)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pnewTarget = hb_itemPutPtr( NULL, (QWidget *) newTarget );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, pnewTarget );
        hb_itemRelease( psender );
        hb_itemRelease( pnewTarget );
      }
    }
  }
}

HB_FUNC( QDRAG_ONACTIONCHANGED )
{
  QObject* object = (QObject*) hb_itemGetPtr( hb_objSendMsg( hb_stackSelfItem(), "POINTER", 0 ) );
  QString signal = "actionChanged(Qt::DropAction)";
  if( s == NULL )
  {
    s = new SlotsQDrag(QCoreApplication::instance());
  }
  bool ret = false;
  if( hb_pcount() == 1 )
  {
    PHB_ITEM codeblock = hb_itemNew( hb_param( 1, HB_IT_BLOCK | HB_IT_SYMBOL ) );
    // verifica se já está na lista
    int found = -1;
    int i;
    for (i = 0; i < s->list1.size(); ++i)
    {
      if( ( (QObject *) s->list1.at(i) == (QObject *) object ) && ( s->list2.at(i) == signal ) && ( (bool) s->list4.at(i) == true ) )
      {
        found = i;
        break;
      }
    }
    // se nao encontrado na lista, conecta e adiciona
    if( found == -1 )
    {
      ret = object->connect( object, SIGNAL(actionChanged(Qt::DropAction)), s, SLOT(actionChanged(Qt::DropAction)) );
      if( ret ) // se conectado, adiciona
      {
        // procura por posição livre
        i = s->list4.indexOf( false );
        if( i == -1 ) // nao encontrou posicao livre
        {
          // adiciona sinal na lista
          s->list1 << object;
          s->list2 << signal;
          s->list3 << codeblock;
          s->list4 << true;
        }
        else // encontrou posicao
        {
          // coloca na posição livre
          s->list1[i] = object;
          s->list2[i] = signal;
          s->list3[i] = codeblock;
          s->list4[i] = true;
        }
      }
    }
  }
  else if( hb_pcount() == 0 )
  {
    ret = object->disconnect(object, SIGNAL(actionChanged(Qt::DropAction)), s, SLOT(actionChanged(Qt::DropAction)) );
    if( ret == true )
    {
      for (int i = 0; i < s->list1.size(); ++i)
      {
        if( (QObject*) s->list1.at(i) == (QObject*) object )
        {
          if( ( (QString) s->list2.at(i) == (QString) signal ) && ( (bool) s->list4.at(i) == true ) )
          {
            hb_itemRelease( (PHB_ITEM) s->list3.at(i) );
            s->list1[i] = NULL;
            s->list2[i] = "";
            s->list3[i] = NULL;
            s->list4[i] = false;
          }
        }
      }
    }
  }
  hb_retl( ret );
}

