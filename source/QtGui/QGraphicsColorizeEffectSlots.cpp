#include "QGraphicsColorizeEffectSlots.h"

static SlotsQGraphicsColorizeEffect * s = NULL;

SlotsQGraphicsColorizeEffect::SlotsQGraphicsColorizeEffect(QObject *parent) : QObject(parent)
{
}
SlotsQGraphicsColorizeEffect::~SlotsQGraphicsColorizeEffect()
{
  QGraphicsColorizeEffect_release_codeblocks();
}
void SlotsQGraphicsColorizeEffect::colorChanged ( const QColor & color )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "colorChanged(QColor)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pcolor = hb_itemPutPtr( NULL, (QColor *) &color );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, pcolor );
        hb_itemRelease( psender );
        hb_itemRelease( pcolor );
      }
    }
  }
}
void SlotsQGraphicsColorizeEffect::strengthChanged ( qreal strength )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "strengthChanged(qreal)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pstrength = hb_itemPutND( NULL, strength );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, pstrength );
        hb_itemRelease( psender );
        hb_itemRelease( pstrength );
      }
    }
  }
}

HB_FUNC( QGRAPHICSCOLORIZEEFFECT_ONCOLORCHANGED )
{
  QObject* object = (QObject*) hb_itemGetPtr( hb_objSendMsg( hb_stackSelfItem(), "POINTER", 0 ) );
  QString signal = "colorChanged(QColor)";
  if( s == NULL )
  {
    s = new SlotsQGraphicsColorizeEffect(QCoreApplication::instance());
  }
  bool ret = false;
  if( hb_pcount() == 1 )
  {
    PHB_ITEM codeblock = hb_itemNew( hb_param( 1, HB_IT_BLOCK | HB_IT_SYMBOL ) );
    // verifica se já está na lista
    int found = -1;
    int i;
    for (i = 0; i < s->list1.size(); ++i)
    {
      if( ( (QObject *) s->list1.at(i) == (QObject *) object ) && ( s->list2.at(i) == signal ) && ( (bool) s->list4.at(i) == true ) )
      {
        found = i;
        break;
      }
    }
    // se nao encontrado na lista, conecta e adiciona
    if( found == -1 )
    {
      ret = object->connect( object, SIGNAL(colorChanged(QColor)), s, SLOT(colorChanged(QColor)) );
      if( ret ) // se conectado, adiciona
      {
        // procura por posição livre
        i = s->list4.indexOf( false );
        if( i == -1 ) // nao encontrou posicao livre
        {
          // adiciona sinal na lista
          s->list1 << object;
          s->list2 << signal;
          s->list3 << codeblock;
          s->list4 << true;
        }
        else // encontrou posicao
        {
          // coloca na posição livre
          s->list1[i] = object;
          s->list2[i] = signal;
          s->list3[i] = codeblock;
          s->list4[i] = true;
        }
      }
    }
  }
  else if( hb_pcount() == 0 )
  {
    ret = object->disconnect(object, SIGNAL(colorChanged(QColor)), s, SLOT(colorChanged(QColor)) );
    if( ret == true )
    {
      for (int i = 0; i < s->list1.size(); ++i)
      {
        if( (QObject*) s->list1.at(i) == (QObject*) object )
        {
          if( ( (QString) s->list2.at(i) == (QString) signal ) && ( (bool) s->list4.at(i) == true ) )
          {
            hb_itemRelease( (PHB_ITEM) s->list3.at(i) );
            s->list1[i] = NULL;
            s->list2[i] = "";
            s->list3[i] = NULL;
            s->list4[i] = false;
          }
        }
      }
    }
  }
  hb_retl( ret );
}

