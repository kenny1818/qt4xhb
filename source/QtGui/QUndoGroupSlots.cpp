void SlotsQUndoGroup::activeStackChanged ( QUndoStack * stack )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "activeStackChanged(QUndoStack*)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pstack = hb_itemPutPtr( NULL, (QUndoStack *) stack );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, pstack );
        hb_itemRelease( psender );
        hb_itemRelease( pstack );
      }
    }
  }
}
