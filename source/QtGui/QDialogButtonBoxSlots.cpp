SlotsQDialogButtonBox::SlotsQDialogButtonBox(QObject *parent) : QObject(parent)
{
}
SlotsQDialogButtonBox::~SlotsQDialogButtonBox()
{
  QDialogButtonBox_release_codeblocks();
}
void SlotsQDialogButtonBox::accepted ()
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "accepted()" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 1, psender );
        hb_itemRelease( psender );
      }
    }
  }
}
void SlotsQDialogButtonBox::clicked ( QAbstractButton * button )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "clicked(QAbstractButton*)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pbutton = hb_itemPutPtr( NULL, (QAbstractButton *) button );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, pbutton );
        hb_itemRelease( psender );
        hb_itemRelease( pbutton );
      }
    }
  }
}
