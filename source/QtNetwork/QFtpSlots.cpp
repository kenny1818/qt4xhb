void SlotsQFtp::commandFinished ( int id, bool error )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "commandFinished(int,bool)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pid = hb_itemPutNI( NULL, id );
        PHB_ITEM perror = hb_itemPutL( NULL, error );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 3, psender, pid, perror );
        hb_itemRelease( psender );
        hb_itemRelease( pid );
        hb_itemRelease( perror );
      }
    }
  }
}
