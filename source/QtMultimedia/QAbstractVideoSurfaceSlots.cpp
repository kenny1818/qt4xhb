SlotsQAbstractVideoSurface::SlotsQAbstractVideoSurface(QObject *parent) : QObject(parent)
{
}
SlotsQAbstractVideoSurface::~SlotsQAbstractVideoSurface()
{
  QAbstractVideoSurface_release_codeblocks();
}
void SlotsQAbstractVideoSurface::activeChanged(bool active)
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "activeChanged(bool)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pactive = hb_itemPutL( NULL, active );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, pactive );
        hb_itemRelease( psender );
        hb_itemRelease( pactive );
      }
    }
  }
}
