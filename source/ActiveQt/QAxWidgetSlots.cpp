#include "QAxWidgetSlots.h"

static SlotsQAxWidget * s = NULL;

SlotsQAxWidget::SlotsQAxWidget(QObject *parent) : QObject(parent)
{
}
SlotsQAxWidget::~SlotsQAxWidget()
{
  QAxWidget_release_codeblocks();
}
void SlotsQAxWidget::exception ( int code, const QString & source, const QString & desc, const QString & help )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "exception(int,QString,QString,QString)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_ITEM pcode = hb_itemPutNI( NULL, code );
        PHB_ITEM psource = hb_itemPutC( NULL, RQSTRING(source) );
        PHB_ITEM pdesc = hb_itemPutC( NULL, RQSTRING(desc) );
        PHB_ITEM phelp = hb_itemPutC( NULL, RQSTRING(help) );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 5, psender, pcode, psource, pdesc, phelp );
        hb_itemRelease( psender );
        hb_itemRelease( pcode );
        hb_itemRelease( psource );
        hb_itemRelease( pdesc );
        hb_itemRelease( phelp );
      }
    }
  }
}
