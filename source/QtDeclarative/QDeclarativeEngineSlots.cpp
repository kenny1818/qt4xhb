#include "QDeclarativeEngineSlots.h"

static SlotsQDeclarativeEngine * s = NULL;

SlotsQDeclarativeEngine::SlotsQDeclarativeEngine(QObject *parent) : QObject(parent)
{
}
SlotsQDeclarativeEngine::~SlotsQDeclarativeEngine()
{
  QDeclarativeEngine_release_codeblocks();
}
void SlotsQDeclarativeEngine::quit ()
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "quit()" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 1, psender );
        hb_itemRelease( psender );
      }
    }
  }
}
void SlotsQDeclarativeEngine::warnings ( const QList<QDeclarativeError> & warnings )
{
  QObject *object = qobject_cast<QObject *>(sender());
  for (int i = 0; i < list1.size(); ++i)
  {
    if( (QObject*) list1.at(i) == (QObject*) object )
    {
      if( ( (QString) list2.at(i) == (QString) "warnings(QList<QDeclarativeError>)" ) && ( (bool) list4.at(i) == true ) )
      {
        PHB_ITEM psender = hb_itemPutPtr( NULL, (QObject *) object );
        PHB_DYNS pDynSym;
        #ifdef __XHARBOUR__
        pDynSym = hb_dynsymFind( "QDECLARATIVEERROR" );
        #else
        pDynSym = hb_dynsymFindName( "QDECLARATIVEERROR" );
        #endif
        PHB_ITEM pwarnings = hb_itemArrayNew(0);
        int i;
        for(i=0;i<warnings.count();i++)
        {
          if( pDynSym )
          {
            #ifdef __XHARBOUR__
            hb_vmPushSymbol( pDynSym->pSymbol );
            #else
            hb_vmPushDynSym( pDynSym );
            #endif
            hb_vmPushNil();
            hb_vmDo( 0 );
            PHB_ITEM pTempObject = hb_itemNew( NULL );
            hb_itemCopy( pTempObject, hb_stackReturnItem() );
            PHB_ITEM pTempItem = hb_itemNew( NULL );
            hb_itemPutPtr( pTempItem, (QDeclarativeError *) new QDeclarativeError ( warnings [i] ) );
            hb_objSendMsg( pTempObject, "NEWFROMPOINTER", 1, pTempItem );
            hb_arrayAddForward( pwarnings, pTempObject );
            hb_itemRelease( pTempObject );
            hb_itemRelease( pTempItem );
          }
        }
        hb_vmEvalBlockV( (PHB_ITEM) list3.at(i), 2, psender, pwarnings );
        hb_itemRelease( psender );
        hb_itemRelease( pwarnings );
      }
    }
  }
}

HB_FUNC( QDECLARATIVEENGINE_ONQUIT )
{
  QObject* object = (QObject*) hb_itemGetPtr( hb_objSendMsg( hb_stackSelfItem(), "POINTER", 0 ) );
  QString signal = "quit()";
  if( s == NULL )
  {
    s = new SlotsQDeclarativeEngine(QCoreApplication::instance());
  }
  bool ret = false;
  if( hb_pcount() == 1 )
  {
    PHB_ITEM codeblock = hb_itemNew( hb_param( 1, HB_IT_BLOCK | HB_IT_SYMBOL ) );
    // verifica se já está na lista
    int found = -1;
    int i;
    for (i = 0; i < s->list1.size(); ++i)
    {
      if( ( (QObject *) s->list1.at(i) == (QObject *) object ) && ( s->list2.at(i) == signal ) && ( (bool) s->list4.at(i) == true ) )
      {
        found = i;
        break;
      }
    }
    // se nao encontrado na lista, conecta e adiciona
    if( found == -1 )
    {
      ret = object->connect( object, SIGNAL(quit()), s, SLOT(quit()) );
      if( ret ) // se conectado, adiciona
      {
        // procura por posição livre
        i = s->list4.indexOf( false );
        if( i == -1 ) // nao encontrou posicao livre
        {
          // adiciona sinal na lista
          s->list1 << object;
          s->list2 << signal;
          s->list3 << codeblock;
          s->list4 << true;
        }
        else // encontrou posicao
        {
          // coloca na posição livre
          s->list1[i] = object;
          s->list2[i] = signal;
          s->list3[i] = codeblock;
          s->list4[i] = true;
        }
      }
    }
  }
  else if( hb_pcount() == 0 )
  {
    ret = object->disconnect(object, SIGNAL(quit()), s, SLOT(quit()) );
    if( ret == true )
    {
      for (int i = 0; i < s->list1.size(); ++i)
      {
        if( (QObject*) s->list1.at(i) == (QObject*) object )
        {
          if( ( (QString) s->list2.at(i) == (QString) signal ) && ( (bool) s->list4.at(i) == true ) )
          {
            hb_itemRelease( (PHB_ITEM) s->list3.at(i) );
            s->list1[i] = NULL;
            s->list2[i] = "";
            s->list3[i] = NULL;
            s->list4[i] = false;
          }
        }
      }
    }
  }
  hb_retl( ret );
}

